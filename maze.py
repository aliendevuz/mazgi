def generate_maze(width, height):
    a = [[' ' for _ in range(width * 2 + 1)] for _ in range(height * 2 + 1)]
    b = [[' ' for _ in range(width * 2 + 1)] for _ in range(height * 2 + 1)]
    a[1][1] = '*'
    a[width * 2 - 1][height * 2 - 1] = '#'
    return a, b
